package com.habitrpg.client.resource;

public class Stats {

    private int gp;
    private int exp;
    private int lvl;
    private int hp;
    private int toNextLevel;
    private int maxHealth;

    public int getGp() {
        return gp;
    }

    public int getExp() {
        return exp;
    }

    public int getLvl() {
        return lvl;
    }

    public int getHp() {
        return hp;
    }

    public int getToNextLevel() {
        return toNextLevel;
    }

    public int getMaxHealth() {
        return maxHealth;
    }
}