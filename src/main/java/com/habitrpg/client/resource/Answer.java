package com.habitrpg.client.resource;

public class Answer {

    private double hp;
    private int exp;
    private int lvl;
    private int gp;
    private double delta;

    public double getHp() {
        return hp;
    }

    public int getExp() {
        return exp;
    }

    public int getLvl() {
        return lvl;
    }

    public int getGp() {
        return gp;
    }

    public double getDelta() {
        return delta;
    }
}
